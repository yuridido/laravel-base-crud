    {
        $data = $request->all();



        $gameNew = new Game;
        $gameNew->nome_gioco =  $data['gioco'];
        $gameNew->casa_editrice =  $data['casa'];
        $gameNew->min_gioc =  $data['min'];
        $gameNew->max_gioc =  $data['max'];

        $gameNew->save();
        
    }


